const I18nState = {
  supported: ["de", "nl", "en"],
  locales: { de: "de-DE", nl: "nl-NL", en: "en-IE" },

  get lang() {
    return localStorage.getItem("elira_lang") || this.detect();
  },

  detect() {
    const n = (navigator.language || "en").slice(0, 2).toLowerCase();
    return this.supported.includes(n) ? n : "en";
  },

  setLang(lang) {
    if (!this.supported.includes(lang)) lang = "en";
    localStorage.setItem("elira_lang", lang);
    document.documentElement.lang = lang;
    this.apply();
    document.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
  },

  t(key) {
    const lang = this.lang;
    return (I18N[lang] && I18N[lang][key]) || (I18N.en[key]) || key;
  },

  formatPrice(cents) {
    const lang = this.lang;
    return new Intl.NumberFormat(this.locales[lang] || "de-DE", {
      style: "currency",
      currency: "EUR"
    }).format((cents || 0) / 100);
  },

  apply(root = document) {
    root.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = this.t(el.getAttribute("data-i18n"));
    });
    root.querySelectorAll("[data-i18n-ph]").forEach(el => {
      el.setAttribute("placeholder", this.t(el.getAttribute("data-i18n-ph")));
    });
    root.querySelectorAll("[data-i18n-aria]").forEach(el => {
      el.setAttribute("aria-label", this.t(el.getAttribute("data-i18n-aria")));
    });
    document.documentElement.lang = this.lang;
  }
};

window.I18nState = I18nState;
